#n*m크기의 행렬 초기화를 리스트컴프리헨션 
a=2 
b=3
narr=[["*"]*a for _ in range(b)] # * 2개를 한 행에 넣고 b만큼 for문 돌리면 2*3크기의 행렬 생김
print(narr)

