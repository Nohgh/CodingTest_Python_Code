#리스트 컴프리헨션 comprehension (이해력)
# 리스트를 초기화하는 방법 중 하나
# 대괄호안에 조건문과 반복문을 넣는 방식으로 리스트 초기화 가능하다 

#0부터 19까지의 수에서 홀수만 저장하는 리스트
arr=[i for i in range(20) if i %2 ==1]
print(arr)

#1부터 9까지의 수의 제곱값을 포함하는 리스트
